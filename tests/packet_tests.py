def test_ECPacketData():
    assert ECPacketData((0x02,[(0x02, 3000),(0x01, 5)])) == '\x02\x02\x04\x03\x02\x0b\xb8\x02\x02\x01\x05'

def test_ECPacket():
    assert ECPacket((0x02, [(0x02, 'test')])) == '\x00\x00\x00\x22\x00\x00\x00\x0A\x02\x01\x04\x06\x05test\x00'

